                                <div class="card-header">
                                    <h3 class="card-title">Transitions Properties</h3>


                                </div>


                                <div class="card-body">
                                    Transitions is used to change property values from one value to another, over a
                                    given duration. The transition effect will start when the specified CSS property
                                    changes value. <br>
                                    <b>How to Use Transitions?</b>
                                    <ul>
                                        - CSS property you want to add an effect to <br>
                                        - Duration of the effect

                                    </ul>
                                    <p><b>Note:</b> If the duration part is not specified, the transition will have no
                                        effect, because the default value is 0.</p>
                                    <ul>
                                        <li>transition-timing-function</li>
                                        <li>transition-property</li>
                                        <li>transition-duration</li>
                                        <li>transition-delay</li>
                                        <li>transition</li>

                                    </ul>


                                    <h5>transition-property</h5>
                                    <p>This property is used to specify the name of the CSS property the transition
                                        effect is for (the transition effect will start when the specified CSS property
                                        changes). Always specify the transition-duration property, otherwise the
                                        duration is 0, and the transition will have no effect. <br>
                                        We can set this property to none, all and property.</p>
                                    <p>Example:</p>
                                    <code>
                                            div {  <br>
                                                transition-property: all;  <br>
                                            } 

                                        </code> <br>
                                    <ul>
                                        <li>none - No property will get a transition effect</li>
                                        <li>all - All properties will get a transition effect. This is Default value.
                                        </li>
                                        <li>property - Defines a comma separated list of CSS property names the
                                            transition effect is for</li>

                                    </ul> <br><br>
                                    <h5>transition-duration</h5>
                                    <p>This property is used to specify how many seconds or milliseconds a transition
                                        effect takes to complete. We can set this property to time in the form of
                                        seconds or milliseconds. The default value of this property is 0s which means
                                        there will be no effect.</p>
                                    <code>
                                            div { <br>
                                                transition-duration: 2s;<br>
                                             }


                                        </code><br><br>

                                    <h5>transition-timing-function</h5>
                                    <p>This property is used to specify the speed curve of the transition effect. This
                                        property allows a transition effect to change speed over its duration. We can
                                        set this property to ease, linear, ease-in, ease-out, ease-in-out, step-start,
                                        step-end, steps(int, start or end), cubic-bezier(n, n, n, n).</p>
                                    <code>
                                        div {<br>
                                             transition-timing-function: ease-in;<br>
                                             }
                                            
                                    </code><br>
                                    <ul>
                                        <li>ease – It Specifies a transition effect with a slow start, then fast, then
                                            end slowly (equivalent to cubic-bezier(0.25, 0.1, 0.25, 1)). This is Default
                                            value. </li>

                                        <li>linear – It specifies a transition effect with the same speed from start to
                                            end (equivalent to cubic-bezier(0, 0, 1, 1))</li>

                                        <li>ease-in – It specifies a transition effect with a slow start (equivalent to
                                            cubic-bezier(0.42, 0, 1, 1))</li>

                                        <li>ease-out – It specifies a transition effect with a slow end (equivalent to
                                            cubic-bezier(0, 0, 0.58, 1))</li>

                                        <li>ease-in-out – It specifies a transition effect with a slow start and end
                                            (equivalent to cubic-bezier(0.42, 0, 0.58, 1))</li>

                                        <li> step-start - Equivalent to steps(1, start)</li>

                                        <li>step-end - Equivalent to steps(1, end)</li>

                                        <li>steps(int, start or end) - Specifies a stepping function, with two
                                            parameters. The first parameter specifies the number of intervals in the
                                            function. It must be a positive integer (greater than 0). The second
                                            parameter, which is optional, is either the value "start" or "end", and
                                            specifies the point at which the change of values occur within the interval.
                                            If the second parameter is omitted, it is given the value "end"</li>

                                        <li>cubic-bezier(n,n,n,n) - Define your own values in the cubic-bezier function.
                                            Possible values are numeric values from 0 to 1</li>
                                    </ul>

                                    <h5>transition-delay</h5>
                                    <p>This property is used to specify when the transition effect will start. We can set this property to time in the form of seconds or milliseconds. </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                             transition-delay: 1000ms; <br>
                                             }

                                    </code><br><br>

                                    <h5>transition</h5>
                                    <p>The transition property is a shorthand property for the four transition properties.</p>
                                    <p>Syntax:</p>
                                    <code>
                                        Selector { <br>
                                             transition: transition-property, transition-duration, transition-timing-function, and transition-delay; <br>
                                             }

                                    </code>

                                    <p>Example:</p>
                                    <code>
                                        div {<br>
                                            transition: width 2000ms ease;<br>
                                         }

                                    </code><br><br>
                                    <p><b>Note:</b> Always specify the transition-duration property, otherwise the duration is 0s, and the transition will have no effect.
                                    </p>



