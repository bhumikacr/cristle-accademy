                                <div class="card-header">
                                    <h3 class="card-title">FlexBox Properties</h3>


                                </div>


                                <div class="card-body">
                                    <ul>
                                        <li> What is it ? <br>
                                            - Its Layout mode a CSS 3 concept </li>

                                        <li>Why we need it ? <br>
                                            - It helps to create responsive web pages</li>

                                    </ul>
                                    <h5>Flexbox Properties</h5>
                                    <ul>
                                        <li>display</li>
                                        <li>flex-direction</li>
                                        <li>justify-content</li>
                                        <li>align-items</li>
                                        <li>flex-wrap</li>
                                        <li>align-content</li>
                                        <li>flex-flow</li>
                                        <li>order</li>
                                        <li>align-self</li>
                                        <li>flex</li>

                                    </ul>
                                    <h5>flex-direction</h5>
                                    <p>This property is used to set the direction of flexible items. If the element is
                                        not a flexible item, the flex-direction property has no effect. We can set this
                                        property to row (default), row-reverse, column, column-reverse.</p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                             flex-direction: column-reverse; <br>
                                             }

                                    </code><br><br>

                                    <ul>
                                        <li>row - This is used to display the flexible items horizontally as row</li>

                                        <li>row-reverse - This is used to display the flexible items same as row, but in
                                            reverse order</li>
                                        <li>column - This is used to display the flexible items vertically as column
                                        </li>
                                        <li>column-reverse - This is used to display the flexible items same as column,
                                            but in reverse order</li>
                                    </ul>

                                    <h5>justify-content</h5>
                                    <p>TThis property is used to align the flexible container's items when the items do
                                        not use all available space on the main-axis (horizontally). We can set this
                                        property to flex-start (default), flex-end, center, space-between, space-around.
                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                             justify-content: center; <br>
                                             }

                                    </code><br><br>

                                    <ul>
                                        <li>flex-start – This is used to position the item at the beginning of the
                                            container</li>

                                        <li>flex-end – This is used to position the item at the end of the container
                                        </li>
                                        <li>center - This is used to position the item at the center of the container
                                        </li>
                                        <li>space-between - This is used to position the item with space between the
                                            lines
                                        </li>
                                        <li>space-around - This is used to position the item with space before, between,
                                            and after the lines
                                        </li>
                                    </ul>

                                    <h5>align-items</h5>
                                    <p>This property is used to specify the default alignment for items inside the
                                        container. This property can be override using align-self property for each
                                        item. We can set this property to stretch (default), center, flex-start,
                                        flex-end, baseline.
                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            align-items: center; <br>
                                             }

                                    </code><br><br>

                                    <ul>
                                        <li>stretch – This is used to stretch the item to fit the container
                                        </li>

                                        <li>center – This is used to position the item at the center of the container
                                        </li>
                                        <li>flex-start - This is used to position the item at the beginning of the
                                            container
                                        </li>
                                        <li>flex-end - This is used to position the item at the end of the container
                                        </li>
                                        <li>baseline - This is used to position the item at the baseline of the
                                            container
                                        </li>
                                    </ul>
                                    <h5>flex-wrap</h5>
                                    <p>This property is used to specify whether the flexible items should wrap or not.
                                        If the elements are not flexible items, the flex-wrap property has no effect. We
                                        can set this property to nowrap (default), wrap and wrap-reverse.
                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            flex-wrap: wrap;  <br>
                                             }

                                    </code><br><br>

                                    <ul>
                                        <li>nowrap – It specifies that the flexible items will not wrap
                                        </li>

                                        <li>wrap – It specifies that the flexible items will wrap if necessa </li>
                                        <li>wrap-reverse – It specifies that the flexible items will wrap, if necessary,
                                            in reverse order
                                        </li>

                                    </ul>

                                    <h5>align-content</h5>
                                    <p>This property is used to modify the behavior of the flex-wrap property. It is
                                        similar to align-items, but instead of aligning flex items, it aligns flex
                                        lines. There must be multiple lines of items for this property to have any
                                        effect. We can set this property to stretch (default), center, flex-start,
                                        flex-end, space-between, space-around.
                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            align-content: center;  <br>
                                             }

                                    </code><br><br>

                                    <ul>
                                        <li>stretch - Lines stretch to take up the remaining space</li>

                                        <li>center - Lines are packed toward the center of the flex container
                                        </li>
                                        <li>flex-start - Lines are packed toward the start of the flex container
                                        </li>
                                        <li>flex-end - Lines are packed toward the end of the flex container
                                        </li>
                                        <li>space-between - Lines are evenly distributed in the flex container
                                        </li>
                                        <li>space-around - Lines are evenly distributed in the flex container, with
                                            half-size spaces on either end
                                        </li>

                                    </ul>

                                    <h5>flex-flow</h5>
                                    <p>TIt is a shorthand property for the flex-direction and the flex-wrap properties.
                                        If the elements are not flexible items, the flex-flow property has no effect.
                                    </p>
                                    <p>Syntax:</p>

                                    <code>
                                        Selector {<br>
                                             flex-flow: flex-direction flex-wrap;<br>
                                            }

                                    </code>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            flex-flow: row-reverse wrap;  <br>
                                             }

                                    </code><br><br>

                                    <h5>order</h5>
                                    <p>This property is used to specify the order of a flexible item relative to the
                                        rest of the flexible items inside the same container. If the element is not a
                                        flexible item, the order property has no effect. We can set this property to
                                        number (default 0).
                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            order: 1;<br>
                                             }

                                    </code><br><br>



                                    <h5>align-self</h5>
                                    <p>This property is used to specify the alignment for the selected item inside the
                                        flexible container. The align-self property overrides the flexible container's
                                        align-items property. We can set this property to auto (default), stretch,
                                        center, flex-start, flex-end, baseline.

                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            align-self: center; <br>
                                             }

                                    </code><br><br>

                                    <ul>
                                        <li>auto - The element inherits its parent container's align-items property, or
                                            "stretch" if it has no parent container
                                        </li>

                                        <li>stretch – This is used to position the element to fit the container

                                        </li>
                                        <li>center - This is used to position the element at the center of the container
                                        </li>
                                        <li>flex-start - This is used to position the element at the beginning of the
                                            container

                                        </li>
                                        <li>flex-end - This is used to position the element at the end of the container
                                        </li>
                                        <li>baseline - This is used to position the element at the baseline of the
                                            container

                                        </li>

                                    </ul>
                                    <h5>flex-grow</h5>
                                    <p>This property is used to specify how much the item will grow relative to the rest
                                        of the flexible items inside the same container. If the element is not a
                                        flexible item, the flex-grow property has no effect. We can set this property to
                                        number (default 0).
                                    </p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            flex-grow: 1; <br>
                                             }

                                    </code><br><br>
                                    <h5>flex-shrink</h5>
                                    <p>This property is used to specify how the item will shrink relative to the rest of
                                        the flexible items inside the same container. If the element is not a flexible
                                        item, the flex-shrink property has no effect. We can set this property to number
                                        (default 1).</p>
                                    <p>Example:</p>
                                    <code>
                                        div { <br>
                                            flex-shrink: 2; <br>
