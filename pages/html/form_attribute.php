                                <div class="card-body">

                                    <ul>
                                        <li>
                                            <h5>Type Attribute</h5>- It indicates the type of input element. It’s an
                                            empty tag. The default value is text. <br>
                                            <code>&lt;input type=“text”></code>
                                        </li> <br><br>
                                        <li>
                                            <h5>Accept-charset Attribute</h5>- It specifies the character encodings that
                                            are to be used for the form submission. <br>
                                            ISO-8859-1 Character encoding for the Latin alphabet <br>
                                            UTF-8 Character encoding for Unicode <br>
                                            <code>&lt;form accept-charset=“UTF-8”></code>

                                        </li><br> <br>
                                        <li>
                                            <h5>Action Attribute </h5>- It contains a URL that defines where to send the
                                            data after submitting the form. <br>
                                            It specifies the physical address of the server to which the user data
                                            should be redirected at the click of the submit button. <br>
                                            <code>action=http://www.something.com/something.php <br>
                                        action=“page.php” <br>
                                        action=“gotform.php” <br>
                                        action=“example.asp” <br>
                                        </code>
                                        </li><br> <br>
                                        <li>
                                            <h5>Autocomplete Attribute</h5>On – The browser will automatically complete
                                            values
                                            based on values that the user has entered before. <br>
                                            Off – User need to write all values each time they fill the form. <br>
                                            <code>&lt;form autocomplete=“on”></code>
                                        </li><br> <br>
                                        <li>
                                            <h5>Enctype Attribute</h5>- It specifies how the browser encodes the data
                                            before it sends it to the server. <br>
                                            <code>&lt;form enctype=“multipart/form-data”>
                                        </code>
                                        </li><br> <br>
                                        <li>
                                            <h5>Method Attribute</h5>- It Specifies how to send the form data to a web
                                            server. The data can be sent as URL variables, by using the get method or as
                                            HTTP post, by using the post method. <br> <br>
                                            <h5>GET</h5>- GET sends the data as part of the URL. <br>
                                            1. Appends form-data into the URL in name/value pairs <br>
                                            2. The length of a URL is limited 2048 characters <br>
                                            3. Never use GET method if you have password or other sensitive information
                                            to be sent to the server. <br>
                                            4. Useful for form submissions where a user want to bookmark the result <br>
                                            5. GET is better for non-secure data, like query strings in Google <br>
                                            6. GET can't be used to send binary data, ex:- images or word documents <br>
                                            <code>&lt;form action=“some.php” method = “GET”></code> <br> <br>

                                            <h5>POST</h5>- HTTP POST requests supply additional data from the client
                                            (browser) to the server in the message body. <br>
                                            1. Appends form-data inside the body of the HTTP request (data is not shown
                                            is in URL) <br>
                                            2. The POST method does not have any restriction on data size to be sent.
                                            <br>
                                            3. Form submissions with POST cannot be bookmarked <br>
                                            4. The POST method can be used to send ASCII as well as binary data. Ex:-
                                            image and word documents etc <br>
                                            5. The data sent by POST method goes through HTTP header so security depends
                                            on HTTP protocol. By using Secure HTTP you can make sure that your
                                            information is secure. <br>
                                            6. POST is a little safer than GET because the parameters are not stored in
                                            browser history or in web server logs <br>
                                            <code>&lt;form action=“some.php” method = “POST”>
                                            </code>
                                        </li><br> <br>
                                        <li>
                                            <h5>Name Attribute</h5>- It is Used to give a name to the control which is
                                            sent to the server to be recognized and get the value. <br>
                                            <code>name=“lastname”
                                        </code>
                                        </li><br> <br>
                                        <li>
                                            <h5>Novalidate Attribute</h5>- If we use novalidate attribute in form tag
                                            then the validation will not take place, even if there is a required filed.
                                            <br>
                                            <code>&lt;form novalidate> <br>
                                                &lt;form novalidate=“novalidate”> <br>
                                                &lt;form novalidate=“true”> <br>
                                                </code>
                                            - There is one more tag formnovalidate which is used by input tag to
                                            speficiy novlidate this particular text or field we will study about this
                                            later.
                                        </li><br> <br>
                                        <li>
                                            <h5>Text Direction (dirname) Attribute</h5>- Specifies that the text
                                            direction will be submitted <br>
                                            <code>&lt;input type=“text” name=“city” dirname=“city.dir”>
                                            </code>
                                        </li><br> <br>
                                    </ul>

